package com.StudentArrayList;
/*
 * 创建一个Student类包含姓名和年龄属性
 * 
 */
public class StudentArrayListDemo {

	private String name;
	private int age;
	public StudentArrayListDemo() {
		
	}
	public StudentArrayListDemo(String name, int age) {
		
		this.name = name;
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}

	
	
	}


	
		

